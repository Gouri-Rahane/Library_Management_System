    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost/Library_Management";
        String user = "root";
        String pwd = "Gouri1955";
        String input = staffidname.getText().trim();

        if (input.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Staff ID or Name");
            return;
        }

        String query;
            query = "DELETE FROM Staff WHERE STAFF_ID = ?";

        try (Connection conn = DriverManager.getConnection(url, user, pwd);
            PreparedStatement pstmt = conn.prepareStatement(query)) {

            pstmt.setString(1, input);
            int rows = pstmt.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Staff member removed from library");
            } else {
                JOptionPane.showMessageDialog(this, "No such staff member available");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_deleteActionPerformed

